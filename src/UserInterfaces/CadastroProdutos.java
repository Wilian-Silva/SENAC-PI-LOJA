    private void editarCadastro() {

        if (txtID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Selecione um item para Editar!");
        } else {

            camposObrigatorios();
            habilitarCampos();
            btnExcluir.setEnabled(false);
            btnSalvar.setEnabled(true);
            btnNovo.setEnabled(false);
            btnPesquisar.setEnabled(false);
        }

    }

    private void salvarCadastro() {
        if (!emptyFields()) {

            Produto p = new Produto();

            if (txtID.getText().equals("")) {
                //codigo NOVO CLIENTE para enviar dados para classe consulta
                p.setCodBarras(txtCodBarras.getText());
                p.setDescricao(txtDescricao.getText());
                p.setCategoria(txtCategoria.getText());
                p.setUndMedida(txtUndMedida.getText());
                p.setCusto(Double.parseDouble(txtCusto.getText()));
                p.setPrecoVenda(Double.parseDouble(txtPrecoVenda.getText()));
                p.setSaldo(Integer.parseInt(txtSaldoAtual.getText()));
                
                // CHECK BOX
                if(cboxControlaSaldo.isSelected()){
                p.setControlaSaldo(true);
                } else p.setControlaSaldo(false);
                
                //TRATAR RADIO BUTTON
                if(rbAtivo.isSelected()){
                p.setStatus("Ativo");
                }else if (rbInativo.isSelected()){
                p.setStatus("Inativo");}

                // MUITO IMPORTANTE CHAMAR ESSE METODO PARA ADCIONAR DADOS A LISTA E DEPOIS CARREGAR NO TABLE
                if (ProdutoDAO.cadastrar(p)) {
                    JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!"); //cadastrato com sucesso
                    limpar();
                    limparCamposObrigatorios();
                    desabilitarCampos();

                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao cadastrar produto!");
                }//erro ao cadastrar    

            } else {

                //codigo EDITAR CLIENTE para enviar dados para classe consulta
                int id = Integer.parseInt(txtID.getText());
                Produto pes = new Produto();
                //codigo NOVO CLIENTE para enviar dados para classe consulta
                pes.setCodBarras(txtCodBarras.getText());
                pes.setDescricao(txtDescricao.getText());
                pes.setCategoria(txtCategoria.getText());
                pes.setUndMedida(txtUndMedida.getText());
                pes.setCusto(Double.parseDouble(txtCusto.getText()));
                pes.setPrecoVenda(Double.parseDouble(txtPrecoVenda.getText()));
                pes.setSaldo(Integer.parseInt(txtSaldoAtual.getText()));

                // CHECK BOX
                if (cboxControlaSaldo.isSelected()) {
                    pes.setControlaSaldo(true);
                } else {
                    p.setControlaSaldo(false);
                }

                //TRATAR RADIO BUTTON
                if (rbAtivo.isSelected()) {
                    pes.setStatus("Ativo");
                } else if (rbInativo.isSelected()) {
                    pes.setStatus("Inativo");
                }

                // MUITO IMPORTANTE CHAMAR ESSE METODO PARA ADCIONAR DADOS A LISTA E DEPOIS CARREGAR NO TABLE
                if (ProdutoDAO.atualizar(id, pes)) {
                    JOptionPane.showMessageDialog(this, "Produto editado com sucesso!");
                    limpar();
                    limparCamposObrigatorios();
                    desabilitarCampos();

                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao editar  produto!");
                }//erro ao cadastrar 
            }

        }

        
    }

   
    
    public void preencherCampos(Produto p) {
        txtID.setText(String.valueOf(p.getId()));
        txtCodBarras.setText(p.getCodBarras());         
        txtDescricao.setText(p.getDescricao());
        txtCategoria.setText(p.getCategoria());
        txtUndMedida.setText(p.getUndMedida());
        txtCusto.setText(String.valueOf(p.getCusto()));
        txtPrecoVenda.setText(String.valueOf(p.getPrecoVenda()));
        txtSaldoAtual.setText(String.valueOf(p.getSaldo()));
        
        
        //TRATAR RADIAL BUTTON E CHECK BOX
        if(p.isControlaSaldo()){ 
        cboxControlaSaldo.setSelected(true);
        } else cboxControlaSaldo.setSelected(false);
                     
        
        if (p.getStatus().equals("Ativo")) {
            rbAtivo.setSelected(true);
            rbInativo.setSelected(false);
        } else if (p.getStatus().equals("Inativo")) {
            rbAtivo.setSelected(false);
            rbInativo.setSelected(true);
        }

        
        btnEditar.setEnabled(true);
        btnExcluir.setEnabled(true);
        btnCancelar.setEnabled(true);
        
    }

    private void excluirCadastro() {

        // Obtém o ID informado no campo txtID e converte para inteiro
        int pos = Integer.parseInt(txtID.getText());

        // Verifica se o ID é válido (maior que 0)
        if (pos > 0) {
            // Exibe uma caixa de diálogo para confirmar a exclusão
            int resposta = JOptionPane.showConfirmDialog(
                    null, // Componente pai (null usa a janela atual)
                    "Deseja realmente excluir o cadastro?", // Mensagem
                    "Confirmação de exclusão", // Título da janela
                    JOptionPane.YES_NO_OPTION // Opções: Sim ou Não
            );

            // Se o usuário clicar em "Sim"
            if (resposta == JOptionPane.YES_OPTION) {
                // Tenta deletar o cadastro com o ID informado
                boolean sucesso = ProdutoDAO.excluir(pos);

                // Mostra mensagem dependendo do sucesso da exclusão
                if (sucesso) {
                    JOptionPane.showMessageDialog(null, "Cadastro excluído com sucesso.");
                    
                    limpar();
                    desabilitarCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao excluir o cadastro.");
                }
            }
        } else {
            // Se o ID for inválido, informa o usuário
            JOptionPane.showMessageDialog(null, "ID inválido para exclusão.");
        }
    }
 



