    private void novoCadastro() {

        camposObrigatorios();
        habilitarCampos();
        
        btnSalvar.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
        limpar();

    }

