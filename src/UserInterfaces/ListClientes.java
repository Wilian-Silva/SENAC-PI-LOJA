    public void montarETornarTabelaConfigurada(JTable tabelaClientes) {
        String[] colunas = {"ID", "Nome", "CPF"};

        DefaultTableModel modelo = new DefaultTableModel(colunas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Impede edi√ß√£o de todas as c√©lulas
            }
        };

        tblClientes.addMouseListener(new java.awt.event.MouseAdapter() {  //metodo para selecionar do duplo clique
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                if (evt.getClickCount() == 2 && tblClientes.getSelectedRow() != -1) {
                    selecionarRegistro();
                }
            }
        });

        List<Cliente> lista = ClienteDAO.listar();

        for (Cliente m : lista) {
            String[] linha = {
                Integer.toString(m.getId()),
                m.getNome(),
                m.getCpf()
            };
            modelo.addRow(linha);
        }

        tabelaClientes.setModel(modelo);

        // Configura a largura das colunas
        tabelaClientes.getColumnModel().getColumn(0).setPreferredWidth(50);  // ID
        tabelaClientes.getColumnModel().getColumn(1).setPreferredWidth(200); // Nome
        tabelaClientes.getColumnModel().getColumn(2).setPreferredWidth(200); // CPF

        // Alinhar o cabe√ßalho √† esquerda
        JTableHeader header = tabelaClientes.getTableHeader();
        DefaultTableCellRenderer renderer = (DefaultTableCellRenderer) header.getDefaultRenderer();
        renderer.setHorizontalAlignment(SwingConstants.LEFT);

    }

    public void pesquisarTabelaNome(String nome) {

        String[] colunas = {"ID", "Nome", "CPF"};

        DefaultTableModel modelo = new DefaultTableModel(colunas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Impede edi√ß√£o de todas as c√©lulas
            }
        };

        List<Cliente> lista = ClienteDAO.listar();

        // Filtra os clientes cujo nome cont√©m o texto buscado (ignorando mai√∫sculas/min√∫sculas)
        for (Cliente m : lista) {
            if (m.getNome().toLowerCase().contains(nome.toLowerCase())) {
                String[] linha = {
                    Integer.toString(m.getId()),
                    m.getNome(),
                    m.getCpf()
                };
                modelo.addRow(linha);
            }
        }

        tblClientes.setModel(modelo);

        // Configura a largura das colunas
        tblClientes.getColumnModel().getColumn(0).setPreferredWidth(50);  // ID
        tblClientes.getColumnModel().getColumn(1).setPreferredWidth(200); // Nome
        tblClientes.getColumnModel().getColumn(2).setPreferredWidth(200); // CPF

        // Alinhar o cabe√ßalho √† esquerda
        JTableHeader header = tblClientes.getTableHeader();
        DefaultTableCellRenderer renderer = (DefaultTableCellRenderer) header.getDefaultRenderer();
        renderer.setHorizontalAlignment(SwingConstants.LEFT);
    }

    private void selecionarRegistro() {

        List<Cliente> listaCompletaDeClientes = ClienteDAO.listar(); //carrega a lista completa de clientes
        int linhaSelecionada = tblClientes.getSelectedRow(); //pega a linha selecionada da tabela

        if (linhaSelecionada >= 0) {
            // Corrigido: converte corretamente o valor da tabela para inteiro
            String idStr = tblClientes.getValueAt(linhaSelecionada, 0).toString();
            int idSelecionado = Integer.parseInt(idStr);

            // Procurando o cliente na lista completa com o ID selecionado
            Cliente clienteSelecionado = null;
            for (Cliente cliente : listaCompletaDeClientes) {
                if (cliente.getId() == idSelecionado) {
                    clienteSelecionado = cliente;
                    break;
                }
            }

            // Se o cliente foi encontrado
            if (clienteSelecionado != null) {
                // Preenche os campos no CadastroClienteFram              
                cadastroFrame.preencherCampos(clienteSelecionado);  // usamos o atributo passado
                dispose(); // Fecha o JDialog de sele√ß√£o
            } else {
                JOptionPane.showMessageDialog(this, "Cliente n√£o encontrado.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela.");
        }
    }

